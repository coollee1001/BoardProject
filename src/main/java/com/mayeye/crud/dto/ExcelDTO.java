package com.mayeye.crud.dto;

public class ExcelDTO {
	private String excel_name;
	private String excel_address;
	private String excel_phone;
	
	
	public String getExcel_name() {
		return excel_name;
	}
	public void setExcel_name(String excel_name) {
		this.excel_name = excel_name;
	}
	public String getExcel_address() {
		return excel_address;
	}
	public void setExcel_address(String excel_address) {
		this.excel_address = excel_address;
	}
	public String getExcel_phone() {
		return excel_phone;
	}
	public void setExcel_phone(String excel_phone) {
		this.excel_phone = excel_phone;
	}
	
	
	
}
